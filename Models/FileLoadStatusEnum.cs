        /// </summary>
        Downloaded = 1,

        /// <summary>
        /// Взят на обработку
        /// </summary>
        InProgress = 2,

        /// <summary>
        /// Пустой
        /// </summary>
        Empty = 3,

        /// <summary>
        /// Обработан
        /// </summary>
        Processed = 4,

        /// <summary>
        /// Ошибка обработки
        /// </summary>
        Error = 5,

        /// <summary>
        /// Подпись не валидна
        /// </summary>
        SignIsNotValid = 6,

        /// <summary>
        /// Повторная обработка
        /// </summary>
        Repeat = 7
    }
